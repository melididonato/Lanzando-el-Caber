
public class Podio {
	int primerPuesto;
	int segundoPuesto;
	int tercerPuesto;
	
	public Podio() {
		this.primerPuesto = 0;
		this.segundoPuesto = 0;
		this.tercerPuesto = 0;
	}
	
	public Podio(int primerPuesto, int segundoPuesto, int tercerPuesto) {
		this.primerPuesto = primerPuesto;
		this.segundoPuesto = segundoPuesto;
		this.tercerPuesto = tercerPuesto;
	}
	
}
